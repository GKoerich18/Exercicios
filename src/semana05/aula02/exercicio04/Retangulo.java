package semana05.aula02.exercicio04;

public class Retangulo {
    private double largura;
    private double comprimento;

    public Retangulo() {

    }

    public Retangulo(
            double largura,
            double comprimento
    ) {
        this.largura = largura;
        this.comprimento = comprimento;
    }
    public Retangulo(
            double comprimento
    ) {
        this.comprimento = comprimento;
    }
}
